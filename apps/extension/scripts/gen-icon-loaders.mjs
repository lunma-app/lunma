/**
 * Generates `src/ui/icon-loaders.generated.ts`: an explicit lazy-import map for
 * the lucide icons the app can actually reach, replacing the wildcard
 * `import.meta.glob('@lucide/svelte/dist/icons/*.svelte')` that emitted a chunk
 * for ALL ~1,711 lucide icons at build time (see ADR 0014).
 *
 * The allowlist is the union of:
 *   1. `ICON_NAMES` — the curated Space-icon catalogue (user-selectable at runtime
 *      via the picker, so every entry must be loadable), and
 *   2. every icon-name string literal used in source (UI icons like `x`,
 *      `chevron-right`, `plus` that live OUTSIDE the curated catalogue),
 * intersected with the set of real lucide icon files (so every generated import
 * resolves and stray non-icon literals are dropped).
 *
 * Run via `pnpm gen:icons` (also runs in `pnpm build`). `icon-loaders.generated.test.ts`
 * guards freshness + coverage in `pnpm verify`.
 */
import { execFileSync } from 'node:child_process';
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const extRoot = join(here, '..');
const repoRoot = join(extRoot, '..', '..');

const ICON_NAMES_FILE = join(extRoot, 'src/shared/icon-names.ts');
const OUT_FILE = join(extRoot, 'src/ui/icon-loaders.generated.ts');
const SRC_DIR = join(extRoot, 'src');

/**
 * Every real lucide icon name (the source of truth for "does this resolve").
 * Resolved lazily via createRequire — pnpm hoists lucide into .pnpm, and keeping
 * this out of module scope lets the guard test import the helpers without fs cost.
 */
function realLucideNames() {
  const require = createRequire(import.meta.url);
  const iconsDir = dirname(require.resolve('@lucide/svelte/icons/anchor'));
  const names = new Set();
  for (const f of readdirSync(iconsDir)) {
    if (f.endsWith('.svelte')) names.add(f.slice(0, -'.svelte'.length));
  }
  if (names.size === 0) throw new Error(`No lucide icons found in ${iconsDir}`);
  return names;
}

/** The curated catalogue from ICON_NAMES (between `[` and `] as const`). */
function curatedNames() {
  const src = readFileSync(ICON_NAMES_FILE, 'utf8');
  const block = src.match(/export const ICON_NAMES\s*=\s*\[([\s\S]*?)\]\s*as const/);
  if (!block) throw new Error('Could not locate ICON_NAMES array in icon-names.ts');
  return [...block[1].matchAll(/'([a-z][a-z0-9-]*)'/g)].map((m) => m[1]);
}

/** Icon-name string literals reachable in source — must match the test's extraction. */
export function extractIconLiterals(text) {
  const found = new Set();
  const single =
    /\bname\s*=\s*["']([a-z][a-z0-9-]*)["']/g; // <Icon name="x">
  const inBraces = /\b(?:name|icon|leadingIcon)=\{\s*["']([a-z][a-z0-9-]*)["']/g; // name={'x'} / icon={'x' as IconName}
  const iconProp = /\b(?:icon|leadingIcon)\s*[:=]\s*["']([a-z][a-z0-9-]*)["']/g; // icon: 'x' / icon="x"
  const ternary = /\?\s*["']([a-z][a-z0-9-]*)["']\s*:\s*["']([a-z][a-z0-9-]*)["']/g; // cond ? 'x' : 'y'
  for (const re of [single, inBraces, iconProp]) {
    for (const m of text.matchAll(re)) found.add(m[1]);
  }
  for (const m of text.matchAll(ternary)) {
    found.add(m[1]);
    found.add(m[2]);
  }
  return found;
}

/** Source files that can ship icons — excludes generated output and tests (neither ships). */
function walk(dir, out = []) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, entry.name);
    if (entry.isDirectory()) walk(p, out);
    else if (
      /\.(svelte|ts)$/.test(entry.name) &&
      !entry.name.endsWith('.generated.ts') &&
      !/\.test\.(ts|svelte)$/.test(entry.name)
    ) {
      out.push(p);
    }
  }
  return out;
}

function sourceLiterals() {
  const found = new Set();
  for (const file of walk(SRC_DIR)) {
    for (const name of extractIconLiterals(readFileSync(file, 'utf8'))) found.add(name);
  }
  return found;
}

/** The resolved allowlist: (curated ∪ source literals) ∩ real lucide, sorted. */
export function computeAllowlist() {
  const real = realLucideNames();
  const candidates = new Set([...curatedNames(), ...sourceLiterals()]);
  return [...candidates].filter((n) => real.has(n)).sort();
}

function render(names) {
  const entries = names
    .map((n) => `  "${n}": () => import("@lucide/svelte/icons/${n}"),`)
    .join('\n');
  return `// @generated by scripts/gen-icon-loaders.mjs — DO NOT EDIT BY HAND.
// Run \`pnpm gen:icons\` to regenerate. Coverage is enforced by
// icon-loaders.generated.test.ts (runs in \`pnpm verify\`). See ADR 0014.
import type { Component } from "svelte";

type IconModule = { default: Component };
export type IconLoader = () => Promise<IconModule>;

/** Lazy per-icon loaders for every lucide icon the app can reach. */
export const iconLoaders: Record<string, IconLoader> = {
${entries}
};
`;
}

function main() {
  const names = computeAllowlist();
  writeFileSync(OUT_FILE, render(names));
  // Normalise to the repo's Biome style so `biome check src` stays green.
  try {
    execFileSync('pnpm', ['exec', 'biome', 'check', '--write', OUT_FILE], {
      cwd: repoRoot,
      stdio: 'ignore',
    });
  } catch {
    console.warn('[gen-icon-loaders] biome formatting skipped (biome not runnable)');
  }
  console.log(`[gen-icon-loaders] wrote ${names.length} icon loaders -> ${OUT_FILE}`);
}

// Only run when invoked directly, not when imported by the guard test.
if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) main();
