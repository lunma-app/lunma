export type IconName = (typeof ICON_NAMES)[number];

/**
 * Default icon used by the V1 → V2 migration for Spaces that pre-date the
 * `icon` field, and by `CreateSpacePopover` as the initial icon selection.
 */
export const DEFAULT_ICON: IconName = 'star';

/**
 * Type-guard: narrow an arbitrary string to IconName when present in the
 * catalogue. Used by the V2 schema and by the runtime read path so unknown
 * names fall back gracefully instead of breaking the render.
 */
export function isIconName(value: string): value is IconName {
  return (ICON_NAMES as readonly string[]).includes(value);
}
